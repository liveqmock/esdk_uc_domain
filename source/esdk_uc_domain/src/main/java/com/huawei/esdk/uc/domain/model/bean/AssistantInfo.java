package com.huawei.esdk.uc.domain.model.bean;

public class AssistantInfo
{

    protected String assistantName;

    protected String assistantNo;

    protected String assistantSex;

    protected String assistantStatus;

    protected String assistantClientNum;

    protected String assistantNumber;

    public String getAssistantName()
    {
        return assistantName;
    }

    public void setAssistantName(String assistantName)
    {
        this.assistantName = assistantName;
    }

    public String getAssistantNo()
    {
        return assistantNo;
    }

    public void setAssistantNo(String assistantNo)
    {
        this.assistantNo = assistantNo;
    }

    public String getAssistantSex()
    {
        return assistantSex;
    }

    public void setAssistantSex(String assistantSex)
    {
        this.assistantSex = assistantSex;
    }

    public String getAssistantStatus()
    {
        return assistantStatus;
    }

    public void setAssistantStatus(String assistantStatus)
    {
        this.assistantStatus = assistantStatus;
    }

    public String getAssistantClientNum()
    {
        return assistantClientNum;
    }

    public void setAssistantClientNum(String assistantClientNum)
    {
        this.assistantClientNum = assistantClientNum;
    }

    public String getAssistantNumber()
    {
        return assistantNumber;
    }

    public void setAssistantNumber(String assistantNumber)
    {
        this.assistantNumber = assistantNumber;
    }

}
